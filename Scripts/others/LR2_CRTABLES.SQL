    F_ID NUMERIC NOT NULL PRIMARY KEY,
    F_DETAILS NUMERIC NOT NULL,
    F_VOLUME NUMERIC NOT NULL,
    F_PRICE NUMERIC NOT NULL,
    F_DATE DATE NOT NULL
);
